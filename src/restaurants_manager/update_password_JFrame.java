    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         sqldatabaseconn conn = new sqldatabaseconn();

        try {

            s =conn.connect().createStatement(ResultSet.TYPE_FORWARD_ONLY,ResultSet.CONCUR_READ_ONLY);
            r =s.executeQuery("select * from login_data");
            r.next();
             username = r.getString("username");
             password =r.getString("password");
            

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        finally {
            try {
                if (r != null) {
                    r.close();

                }
            } catch (SQLException ex) {
                System.out.println(ex.getMessage());
            }

            if(conn.connect()!= null){
                try{
                    conn.connect().close();
                } catch(Exception e){
                    e.printStackTrace();
                }
            }

        }
            String old_name = jTextField_old_username.getText();
            String old_pass = jTextField_old_password.getText();
            String new_name = jTextField_new_username.getText();
            String new_pass = jTextField_new_password.getText();

            if (username.equals(old_name)&&password.equals(old_pass))
            {
                jLabel_erorr.setText("");
                 if (new_name.equals("")&&new_pass.equals(""))
            {
                jLabel_erorr.setText("بيانات المستخدم الجديد فارغة");
            }
                 else{
                     sqldatabaseconn update_login =new sqldatabaseconn();
                     update_login.update_login_data("login_data",1, new_name, new_pass);
                     jLabel_erorr.setText("تم تحديث بيانات المستخدم");
                      jTextField_old_username.setText("");
                      jTextField_old_password.setText("");
                      jTextField_new_username.setText("");
                      jTextField_new_password.setText("");
                 
                 }

            }
            else
            {
                jLabel_erorr.setText("بيانات المستخدم القديم ليست صحيحة");
                jTextField_old_username.setText("");
                jTextField_old_password.setText("");
            }

    }//GEN-LAST:event_jButton1ActionPerformed

