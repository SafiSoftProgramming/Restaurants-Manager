    private void jComboBox_allaria_costItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox_allaria_costItemStateChanged

        int index = jComboBox_allaria_cost.getSelectedIndex();
        
        
        if(index == 0){
            jTextField_addnew_aria.setEnabled(true);
             jTextField_addnew_ariacost.setEnabled(true);
             jButton_addnewaria_save.setEnabled(true);
 
        }else{
            jTextField_addnew_aria.setEnabled(false);
             jTextField_addnew_ariacost.setEnabled(false);
             jButton_addnewaria_save.setEnabled(false);
        }

        sqldatabaseconn conn = new sqldatabaseconn();
        try{
            s =conn.connect().createStatement(ResultSet.TYPE_FORWARD_ONLY,ResultSet.CONCUR_READ_ONLY);
            r =s.executeQuery("select * from aria_and_cost WHERE _id = "+index);
            r.next();
            String aria= r.getString("aria");
            int cost = r.getInt("cost");
            String costs = Integer.toString(cost);
            jTextField_chosen_aria.setText(aria);
            jTextField_chosen_cost.setText(costs);
            

        }
        catch(Exception e){System.out.println(e);}
        finally {
            if(r != null){
                try{
                    r.close();        
                } catch(Exception e){                 
                }
            }
            if(conn != null){
                try{
                    conn.connect().close();
                } catch(Exception e){
                }
            }
        }

    }//GEN-LAST:event_jComboBox_allaria_costItemStateChanged

