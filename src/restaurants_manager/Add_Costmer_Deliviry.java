    private void jButton_addnewaria_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addnewaria_saveActionPerformed

        int cost = 0 ;
        String aria= jTextField_addnew_aria.getText();
        try{
            cost = Integer.parseInt(jTextField_addnew_ariacost.getText());
        }
        catch(Exception e){jLabel_aria_added.setText("لقد أدخلت حروف في خانة التكلفة ...!");}
        if(aria.equals("")||cost==0)
        {
            jLabel_aria_added.setText("أدخل بيانات المنطقه...!");
        }
        else{
            sqldatabaseconn add_Aria = new sqldatabaseconn();
            add_Aria.insert_Aria("aria_and_cost",aria,cost);
            jLabel_aria_added.setText("تم تسجيل منطقه جديده");
            jComboBox_allaria_cost.setSelectedIndex(0);
            jTextField_chosen_aria.setText(aria);
            jTextField_chosen_cost.setText(jTextField_addnew_ariacost.getText());
            jTextField_addnew_aria.setText("");
            jTextField_addnew_ariacost.setText("0");
        }
        
    }//GEN-LAST:event_jButton_addnewaria_saveActionPerformed

